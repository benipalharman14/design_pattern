package behavioral.command;

public class TV {

    void turnOn(){
        System.out.println("TV turned On");
    }

    void turnOff(){
        System.out.println("TV turned Off");
    }

    void volumeUp(){
        System.out.println("TV Volume increased");
    }

    void volumeDown(){
        System.out.println("TV Volume decreased");
    }

}
